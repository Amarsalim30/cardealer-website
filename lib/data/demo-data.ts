function makeImages(
  vehicleId: string,
  title: string,
  urls: string[],
): VehicleImage[] {
  return urls.map((imageUrl, index) => ({
    id: `${vehicleId}-image-${index + 1}`,
    vehicleId,
    imageUrl,
    altText: `${title} exterior view ${index + 1}`,
    sortOrder: index,
    isHero: index === 0,
    cloudinaryPublicId: null,
    createdAt: now,
  }));
}

function makeVehicle(
  partial: Omit<Vehicle, "location" | "images" | "heroImageUrl"> & {
    imageKey: keyof typeof imageCatalog;
  },
): Vehicle {
  const images = makeImages(partial.id, partial.title, imageCatalog[partial.imageKey]);
  const location = locations.find((item) => item.id === partial.locationId) || null;

  return {
    ...partial,
    images,
    heroImageUrl: images[0]?.imageUrl ?? null,
    location,
  };
}

